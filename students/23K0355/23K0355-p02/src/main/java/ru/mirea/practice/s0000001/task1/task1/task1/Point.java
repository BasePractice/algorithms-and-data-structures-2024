package ru.mirea.practice.s0000001.task1.task1.task1;

public class Point {
    private double x;
    private double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    @Override
    public String toString() {
        return "Точка(" + x + ", " + y + ")";
    }
}
