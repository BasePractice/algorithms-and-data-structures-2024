package ru.mirea.practice.s230k754.lab3.taskmathrand.task2;

public class Point {
    private double x;
    private double y;

    public  Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getY() {
        return y;
    }

    public double getX() {
        return x;
    }
}
