package ru.mirea.practice.s23k0505.task4;

public class Comp {
    private String name;
    private int ram;

    public Comp(String name, int ram) {
        this.name = name;
        this.ram = ram;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getRam() {
        return ram;
    }

    public void setRam(int ram) {
        this.ram = ram;
    }
}
