package ru.mirea.practice.s23k0114.lab2;

public class Task1Author {

    private String name;
    private String email;
    private char gender;

    public Task1Author(String name, String email, char gender) {
        this.name = name;
        this.email = email;
        this.gender = gender;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public char getGender() {
        return gender;
    }

    public void setGender(char gender) {
        this.gender = gender;
    }

    @Override
    public String toString() {
        return "Author's name:\t" + name
                + "\nAuthor's email:\t" + email
                + "\nAuthor's gender\t" + gender;
    }
}
